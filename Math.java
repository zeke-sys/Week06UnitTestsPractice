package week06UnitTestsPractice;

public class Math {
	
	public int multiply(int a, int b) { //method that multiplies two ints and returns an int
		return a * b;
	}
	
	public double divide(int a, int b) { //method that divides two ints and returns a double
		return a / b;
	}
	
	public boolean isPositiveNumber(int a) { //method that returns true if int is positive
		return a > 0;
	}
	
	
} //end of class
